// Helper function to get page by title
function get_page_by_title_custom($title) {
    global $wpdb;
    $page = $wpdb->get_row(
        $wpdb->prepare(
            "SELECT * FROM $wpdb->posts WHERE post_title = %s AND post_type = 'page' AND post_status = 'publish' LIMIT 1",
            $title
        )
    );
    return $page;
}

echo "Adding content to pages...\n\n";

foreach ($page_contents as $title => $content) {
    $page = get_page_by_title_custom($title);
    if ($page) {
        wp_update_post([
            'ID' => $page->ID,
            'post_content' => $content
        ]);
        echo "Updated content for: {$title}\n";
    } else {
        echo "Page not found: {$title}\n";
    }
}

echo "\nContent update complete!\n";
